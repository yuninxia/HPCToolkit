METHOD_FN(display_events)
{
  printf("===========================================================================\n");
  printf("Available synchronous events\n");
  printf("===========================================================================\n");
  printf("Name\t\tDescription\n");
  printf("---------------------------------------------------------------------------\n");
  printf("SYNC\t\t" "The number of synchronous metric slots allocated,\n"
         "\t\teg, SYNC@3 would generate 3 slots\n");
  printf("\n");
}


